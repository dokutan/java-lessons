public class Operators {

	public static void main(String[] args) {
		
		// variables
		int x1 = 18;
		int x2 = 7;
		
		// print result
		System.out.println("Arithmetic operations with integers:");
		System.out.println(x1 +  " + " + x2 + " = " + (x1+x2));
		System.out.println(x1 +  " - " + x2 + " = " + (x1-x2));
		System.out.println(x1 +  " * " + x2 + " = " + (x1*x2));
		System.out.println(x1 +  " / " + x2 + " = " + (x1/x2));
		System.out.println(x1 +  " % " + x2 + " = " + (x1%x2));
		
	}

}
