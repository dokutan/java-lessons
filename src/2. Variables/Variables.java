public class Variables {

	public static void main(String[] args) {
		
		// declaration
		int Integer;
		double Double;
		float Float;
		char Character;		
		// assign initial values
		Integer = 3;
		Double = 3.5;
		Float = 4.5f;
		Character = 'a';
		
		// print everything
		System.out.println("Some variables:");
		System.out.println("Integer:\t" + Integer);
		System.out.println("Double:\t\t" + Double);
		System.out.println("Float:\t\t" + Float);
		System.out.println("Character:\t\t" + Character);
	}

}
